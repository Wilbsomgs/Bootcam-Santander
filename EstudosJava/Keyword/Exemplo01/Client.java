package EstudosJava.Keyword.Exemplo01;

public class Client {
    private String name;
    private int age;

    protected String getName() {
        return name;
    }
    protected void setName(String name) {
        this.name = name;
    }
    protected int getAge() {
        return age;
    }
    protected void setAge(int age) {
        this.age = age;
    }

    
}
